//思路是通过字符计数，从0开始，当读取8个数时，刚好i变成8；然后换行
#include<stdio.h>
#include<string.h>
int main()
{
    char a;
    int i=0;
    while(scanf("%c",&a)!='#')
    {
        printf("%c-%d  ",a,a);
        i++;
        if(i%8==0)  printf("\n");
    }
    return 0;
}
