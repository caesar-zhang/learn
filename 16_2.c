#include<stdio.h>
#define AVE(X,Y) (1.0/(((1.0/(X))+(1.0/(Y)))/2.0))
int main()
{
    int a,b;
    float x;
    scanf("%d%d",&a,&b);
    x=AVE(a,b);
    printf("%.2f",x);
    return 0;
}
