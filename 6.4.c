#include<stdio.h>
int main()
{
    int i,j;
    char a='A';
    for(i=1;i<=6;i++)
    {
        for(j=1;j<=i;j++)
        {
            printf("%c",a);
            a=a+1;
        }
        printf("\n");
    }
    return 0;
}
